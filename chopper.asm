DoChopper       .proc
;v_???          .var ADR1_I
;v_???          .var TEMP1_I
;v_???          .var TEMP2_I
;v_???          .var TEMP3_I
;v_???          .var TEMP4_I
;---

                lda CHOPPER_STATUS
                cmp #kOFF
                bne _1

_XIT            rts

_leap           jmp _8

_1              cmp #kCRASH
                beq _XIT

                cmp #kLAND
                beq _2

                cmp #kPICKUP
                beq _2

                lda FRAME
                and GRAV_SKL
                bne _2

                inc CHOPPER_Y
_2              lda CHOPPER_COL
                beq _3

                cmp #4
                beq _leap               ; LASER

                cmp #8
                bne _4                  ; HYPER

                lda LEVEL
                bne _leap

                sta CHOPPER_COL
                lda #HYPERSPACE_MODE
                sta MODE

                lda #1
                sta SND3_VAL
                sta SND5_VAL
_3              ldx #kFLY
                bra _9

_4              lda CHOP_X
                sta TEMP1_I
                lda CHOP_Y
                sta TEMP2_I
                jsr ComputeMapAddrI

                lda #0
                sta TEMP3_I
                sta TEMP4_I
                jsr CheckLanding

                inc ADR1_I+1
                jsr CheckLanding

                inc ADR1_I+1
                jsr CheckLanding

                lda CHOPPER_COL
                and #%11110000
                bne _8

                lda CHOPPER_COL
                and #%00000010
                beq _5

                jsr SlavePickUp
                bcc _5

                ldx #kPICKUP
                jmp _9

_5              lda TEMP3_I
                cmp #3
                beq _8

                dec CHOPPER_Y
                ldx FUEL_STATUS
                lda CHOP_Y
                cmp #10+4
                blt _6

                cpx #kEMPTY
                beq _8

_6              cpx #kREFUEL
                beq _7

                lda TEMP4_I
                bne _7

                jsr RestorePoint

_7              ldx #kLAND
                bra _9

_8              lda #20
                sta TIM3_VAL
                lda #1
                sta SND3_VAL
                ldx #kCRASH
_9              stx CHOPPER_STATUS
                rts
                .endproc


