UpdateChopper   .proc
;v_???          .var ADR1_I
;v_???          .var TEMP1_I
;v_???          .var TEMP2_I
;---

                lda CHOPPER_STATUS
                cmp #kBEGIN
                beq _1

                cmp #kOFF
                bne _2

                lda #0
                .sta_spr_xpos 0
                .sta_spr_xpos 1
                rts

_1              lda #kFLY
                sta CHOPPER_STATUS
                jmp _CCXY

_2              ldy OCHOPPER_Y
                ldx #17
                lda #0
_next1          sta PLAYER+PL0,y
                sta PLAYER+PL1,y
                iny
                dex
                bpl _next1

                lda CHOPPER_X
                .sta_spr_xpos 0
                clc
                adc #8
                .sta_spr_xpos 1

                lda CHOPPER_ANGLE
                asl
                tax
                lda CHOPPER_SHAPES,x
                sta ADR1_I
                lda CHOPPER_SHAPES+1,x
                sta ADR1_I+1

                lda #0
                sta TEMP1_I
                lda #18
                sta TEMP2_I

                ldx CHOPPER_Y
                stx OCHOPPER_Y

_next2          ldy TEMP1_I
                lda (ADR1_I),y
                sta PLAYER+PL0,x
                ldy TEMP2_I
                lda (ADR1_I),y
                sta PLAYER+PL1,x

                inc TEMP1_I
                inc TEMP2_I
                inx

                lda TEMP1_I
                cmp #18
                bne _next2

                lda CHOPPER_STATUS
                cmp #kCRASH
                bne _5

                ldx CHOPPER_Y
                ldy #18
_next3          lda PLAYER+PL0,x
                and SID_RANDOM
                sta PLAYER+PL0,x
                lda PLAYER+PL1,x
                and SID_RANDOM
                sta PLAYER+PL1,x
                inx
                dey
                bne _next3

                ;!! inc PCOLR0
                ;!! inc PCOLR1
                lda SID_RANDOM
                ora #$F
                sta BAK2_COLOR
                lda MODE
                cmp #GO_MODE
                bne _5

                lda FRAME
                and #1
                bne _3

                inc CHOPPER_Y
_3              dec TIM3_VAL
                bne _5

                lda R_STATUS
                cmp #kOFF
                beq _4

                jsr PositionRobot

                lda #kOFF
                sta R_STATUS
_4              jsr PositionChopper

                lda #NEW_PLAYER_MODE
                sta MODE

_5              lda FRAME
                and #3
                bne _6

                lda CHOPPER_ANGLE
                eor #1
                sta CHOPPER_ANGLE

_6              lda MODE
                cmp #GO_MODE
                bne _XIT

                jsr PositionChopper

_CCXY           lda CHOPPER_X
                sec
                sbc #24
                lsr
                lsr
                clc
                adc SX
                sta CHOP_X
                lda CHOPPER_Y
                sec
                sbc #76+12
                lsr
                lsr
                lsr
                sta TEMP1_I
                lda SY
                bpl _7

                lda #0
_7              clc
                adc TEMP1_I
                sta CHOP_Y
                jmp PositionChopper

_XIT            rts
                .endproc


