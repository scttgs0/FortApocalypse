Hover           .proc
                lda FRAME
                and #7
                bne _XIT

                lda CHOPPER_ANGLE
                cmp #4
                blt _1

                cmp #14
                blt _XIT

_1              cmp #8
                bge _2

                inc CHOPPER_ANGLE
                inc CHOPPER_ANGLE
                rts

_2              dec CHOPPER_ANGLE
                dec CHOPPER_ANGLE
_XIT            rts
                .endproc

