CheckChrI       .proc
;v_???          .var ADR1_I
;v_???          .var ADR2_I
;---

                jsr ComputeMapAddrI

                ldy #0
                lda (ADR1_I),y
                and #$7F
                ldy #0
                sty ADR2_I+1
                asl
                rol ADR2_I+1
                asl
                rol ADR2_I+1
                asl
                rol ADR2_I+1

                clc
                adc #<CHR_SET2
                sta ADR2_I
                lda #>CHR_SET2
                adc ADR2_I+1
                sta ADR2_I+1

                ldy #7
_next1          lda (ADR2_I),y
                bne _XIT

                dey
                bpl _next1

                clc
                rts

_XIT            sec
                rts
                .endproc


